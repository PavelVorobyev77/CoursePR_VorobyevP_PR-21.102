package com.example.coursepr_vorobyevp_pr_21102;

public class Recording {
    private String name;
    private String path;

    public Recording(String name, String path) {
        this.name = name;
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public String getPath() {
        return path;
    }
}
